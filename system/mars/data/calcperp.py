import sys
import os.path

#======================================================================
# -------------------------------------------------
# SETTINGS
# -------------------------------------------------

#======================================================================
# -------------------------------------------------
# Init
# -------------------------------------------------

output1 = open("perspdata_min.bin","wb")
output2 = open("perspdata_max.bin","wb")

#======================================================================
# -------------------------------------------------
# Picture
# -------------------------------------------------

PERSP = 160.00
Z = -512.00

for this in range(0,512):
	a = float(Z/PERSP)*0x100
	mars = int(a)
	output1.write(bytes([
		mars >> 24 & 0xFF,
		mars >> 16 & 0xFF,
		mars >> 8 & 0xFF,
		mars & 0xFF,
		]))
	Z += 1.5

for this in range(0,512):
	a = float(Z/PERSP)*0x100
	mars = int(a)
	output2.write(bytes([
		mars >> 24 & 0xFF,
		mars >> 16 & 0xFF,
		mars >> 8 & 0xFF,
		mars & 0xFF,
		]))
	Z += 1.0
	
#======================================================================
# ----------------------------
# End
# ----------------------------

print("Done.")
output1.close()
output2.close()
